package Adventure_generator.POJO;

public class AdventureIdea {
    private String mood;
    private String weather;
    private String adventure;
    private String longDistance;
    
    public String getMood() {
        return mood;
    }
    public void setMood(String mood) {
        this.mood = mood;
    }
    public String getWeather() {
        return weather;
    }
    public void setWeather(String weather) {
        this.weather = weather;
    }
    public String getAdventure() {
        return adventure;
    }
    public void setAdventure(String adventure) {
        this.adventure = adventure;
    }
    public String getLongDistance() {
        return longDistance;
    }
    public void setLongDistance(String longDistance) {
        this.longDistance = longDistance;
    }


}
